            <p>Le informamos de que en <a href="https://spotdesking.spotlinker.com/">spotdesking.spotlinker.com</a> utilizamos
                cookies con objeto de facilitar el uso y navegación a través de la página web y mejorar la calidad de la
                página de acuerdo a los hábitos y estilos de navegación de los usuarios.</p>
            <h2>1. ¿Qué son las cookies?</h2>
            <p>Son ficheros que se descargan en el ordenador, Smartphone o tableta del usuario cuando accede a una
                página web y que permiten al titular de dicha página almacenar o recuperar determinada información sobre
                múltiples variables, como por ejemplo; el número de veces que la página ha sido visitada por el usuario,
                garantizar la sesión del usuario mientras navega por la página, etc. </p>
            <h2>2. Tipología de cookies</h2>
            <p>A continuación se ofrece información sobre el tipo de cookies y la finalidad de las mismas:</p>
            <h3>2.1. Tipos de cookies según la entidad que las gestione</h3>
            <p> Según quien sea la entidad que gestione el equipo o dominio desde donde se envían las cookies y trate
                los datos que se obtengan, podemos distinguir:<br> Cookies propias: Son aquellas que se envían al equipo
                terminal del usuario desde <a href="https://spotdesking.spotlinker.com./">spotdesking.spotlinker.com.</a> <br>
                Cookies de terceros: Son aquellas que se envían al equipo terminal del usuario desde un equipo o dominio
                que no es gestionado por el editor, sino por otra entidad que trata los datos obtenidos través de las
                cookies, como <a href="http://www.google.com./">www.google.com.</a> </p>
            <h3>2.2. Tipos de cookies según el plazo de tiempo que permanecen activadas en el equipo </h3>
            <p>Cookies de sesión: Son un tipo de cookies diseñadas para recabar y almacenar datos mientras el usuario
                accede a una página web. Estas cookies se almacenan en el terminal hasta que finalice la sesión de
                navegación del usuario. </p>
            <p> Cookies persistentes: Son un tipo de cookies en el que los datos siguen almacenados en el terminal y
                pueden ser accedidos y tratados durante un periodo definido por el responsable de la cookie, que puede
                ir de unos minutos a varios años.</p>
            <h3>2.3. Tipos de cookies según su finalidad</h3>
            <p> Cookies técnicas: Permiten al usuario la navegación a través de la página web o la plataforma y la
                utilización de las diferentes opciones o servicios que en ella existan como, por ejemplo, controlar el
                tráfico y la comunicación de datos, identificar la sesión, acceder a partes de acceso restringido,
                utilizar elementos de seguridad durante la navegación o almacenar contenidos para la difusión de videos
                o sonido o compartir contenidos a través de redes sociales. Se trata de cookies de sesión y
                persistentes.</p>
            <p> Cookies de personalización: Permiten al usuario acceder al servicio con algunas características
                predefinidas en función de una serie de criterios en el terminal del usuario como por ejemplo, el tipo
                de navegador a través del cual accede al servicio, la configuración regional desde donde accede al
                servicio, etc. Terceros como Google, Youtube o Twitter usan este tipo de cookies de personalización
                persistentes respecto de sus servicios.</p>
            <p> Cookies analíticas: Son aquellas que permiten cuantificar el número de usuarios y así realizar la
                medición y análisis estadístico de la utilización que hacen los usuarios de nuestra web. Para ello se
                analiza su navegación en nuestra página web con el fin de mejorarla. En <a
                    href="https://spotdesking.spotlinker.com/">spotdesking.spotlinker.com.</a> se utilizan cookies analíticas,
                propias, de sesión y persistentes, para esta finalidad, concretamente cookies de Google Analytics que
                tienen distintos plazos de caducidad en función de la información que nos ofrecen. Terceros como Google,
                Youtube o Twitter también usan este tipo de cookies analíticas de sesión y persistentes.</p>
            <h2>3. ¿Quién utiliza las cookies?</h2>
            <p>La información que se recaba a través de las cookies de una página web puede ser utilizada tanto por el
                titular de la página web o por un tercero que preste un servicio al titular.</p>
            <p>En este sentido, la información de las cookies utilizadas en <a href="https://spotdesking.spotlinker.com/">
                    spotdesking.spotlinker.com</a> es tratada por SpotLinker y
                por un tercero, en concreto, Google a través de su servicio de Google Analytics. Así, la
                página web de SpotLinker utiliza un servicio de analítica
                web que instala cookies temporalmente con la única y exclusiva finalidad de elaborar informes de
                carácter estadístico (Google Analytics). La principal cookie que utiliza Google Analytics es “_ga”. </p>
            <h2>4. Gestión de las cookies</h2>
            <p>Teniendo en cuenta la información facilitada a través de la presente Política de cookies, a continuación
                le ofrecemos información sobre cómo puede llevar a cabo la gestión de las cookies utilizadas en <a
                    href="https://spotdesking.spotlinker.com/">spotdesking.spotlinker.com.</a> a través de las diferentes opciones
                que ofrecen los navegadores más comunes (Explorer, Firefox y Chrome). Si se impide la instalación de
                todas las cookies de la página web, algunas funcionalidades de la misma, así como algunos contenidos
                pueden verse afectados.</p>
