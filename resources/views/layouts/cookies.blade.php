            <h3>4.2. Cómo desactivar o eliminar las cookies</h3>
            <p>Para desactivar, restringir, bloquear o eliminar las cookies, el usuario deberá modificar la
                configuración del navegador que utilice en su terminal, ya sea ordenador, smartphone o tablet. Con
                carácter general los navegadores ofrecen las siguientes opciones de configuración en relación a la
                instalación de cookies:</p>
            <p>Que el navegador rechace todas las cookies y por lo tanto, que no se instale ninguna cookie de ninguna
                página web en su terminal.</p>
            <p>Que el navegador le avise antes de que se produzca la instalación de la cookie para que usted decida si
                acepta o no la instalación de la misma.</p>
            <p>Que el navegador únicamente rechace las cookies de terceros de los sitios web que visita pero no las
                utilizadas por el sitio web por el que usted navega.</p>
